#__________________________________________________________________________

# 함수이름 활용법
# - 함수 코드의 시작주소를 저장하는 변수명
# - 타입 : class func

#__________________________________________________________________________

name = "hong"
alias = name

print(f"name -> {id(name)}, alias -> {id(alias)}")  # 같은 주소명
print(name, alias)

#__________________________________________________________________________

# 함수 이름 : 함수코드의 시작 주소 저장

#__________________________________________________________________________

total = sum

print(f"total -> {id(total)}, sum -> {id(sum)}")  # 같은 주소명
print(total([1, 2, 3]), sum([1, 2, 3]))

#__________________________________________________________________________

# 함수 이름을 변수명으로 사용한 경우 -> 호출 불가

#__________________________________________________________________________

# max = 100  # <- 함수 코드 시작 주소가 아니고, int 100의 주소를 저장하게 됨!
