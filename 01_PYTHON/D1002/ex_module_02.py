#__________________________________________________________________________
# 모듈 사용하기
# - 모듈 : 특정 목적에 관련된 변수, 함수, 클래스 등이 있는 파일(py)
# - 사용 : import 모듈명
#__________________________________________________________________________
# 모듈 로딩
#__________________________________________________________________________
from math import pi, factorial      # 모듈 내 일부분만 로딩
from random import *                # 모듈 내 모든 것 로딩
#__________________________________________________________________________
# 전역 변수
#__________________________________________________________________________
pi = "파이값 3.14"
#__________________________________________________________________________
# 모듈 일부분 -> 변수명, 함수명(), 클래스명
#__________________________________________________________________________
print(f"pi -> {pi}")
print(f"factorial(3) -> {factorial(3)}")
print(f"factorial(1, 3) -> {randint(1, 3)}")
print(f"random() -> {random()}")