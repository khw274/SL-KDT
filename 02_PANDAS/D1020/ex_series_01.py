#__________________________________________________________________________
# Series 인스턴스/객체 생성
#__________________________________________________________________________
# -> 모듈 로딩
#__________________________________________________________________________
import pandas as pd
#__________________________________________________________________________
# -> 데이터 준비
#__________________________________________________________________________
jumsu = [99, 77, 55, 88, 66]

#__________________________________________________________________________
# -> Series로 변환 저장
#__________________________________________________________________________
dataSR = pd.Series(jumsu) # 시리즈 생성
#__________________________________________________________________________
# -> List와 Series 데이터 표현/저장 방식 비교
#__________________________________________________________________________
print("---[LIST 방식]---")
print(jumsu)


print("\n---[Series 방식]---")
print(dataSR)
#__________________________________________________________________________
# -> Series 인스턴스의 속성 읽기
#__________________________________________________________________________
print("\n---[Series 속성 읽기]---")
print(f"인덱스: {dataSR.index}")
print(f"데이터: {dataSR.values}, {type(dataSR.values)}")
print(f"형  태: {dataSR.shape}") # 1차원으로 원소 수 반환
print(f"차  원: {dataSR.ndim}차원")  # 1차원
print(f"타  입: {dataSR.dtype}")  

