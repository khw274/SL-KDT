// 5. 함수 선언 
// -------------------------------------------------------------------
void change_x(char** px);

// -------------------------------------------------------------------
// 6. 메인 함수 
// -------------------------------------------------------------------
int main(void)
{
	char* x = "AAA";		// 문자형 포인터 x에 "AAA"의 첫글자 주소 저장
	printf("%s\n", x);		// x가 가리키는 문자열 "AAA" 출력

	change_x(&x);			// x의 주소를 change_x 함수의 이중포인터 px에 전달

	printf("%s", x);		// "ZZZ"의 주소에 있는 "ZZZ" 문자열 출력

	return 0;
}
// -------------------------------------------------------------------
// 7. 함수 구현 
// -------------------------------------------------------------------
void change_x(char** px)	// char** px = &x, px = x(즉 포인터의 주소), *px = x의 값
{
	*px = "ZZZ";			// *px = x 에 "ZZZ"의 첫글자 주소 저장
}