int main(void)
{

	// 변수 선언
	int    a = 10;
	int* p = &a;		// 정수형 포인터
	int** q = &p;		// 2중    포인터
	int*** r = &q;		// 3중    포인터

	printf("------------- int a = 10 -------------\n");
	printf("  &a ------ a의 주소 : %p \n", &a);
	printf("   a ------ 정수 a   : %d \n\n", a);

	printf("------------- int *p = &a -------------\n");
	printf("  &p ------ p의 주소 : %p \n", &p);
	printf("   p ------ a의 주소 : %p \n", p);
	printf("  *p ------ a의 값   : %d \n\n", *p);

	printf("------------- int **q = &p -------------\n");
	printf("  &q ------ q의 주소 : %p \n", &q);
	printf("   q ------ p의 주소 : %p \n", q);
	printf("  *q ------ a의 주소 : %p \n", *q);
	printf(" **q ------ a의 값   : %d \n\n", **q);

	printf("------------- int ***r = &q -------------\n");
	printf("  &r ------ r의 주소 : %p \n", &r);
	printf("   r ------ q의 주소 : %p \n", r);
	printf("  *r ------ p의 주소 : %p \n", *r);
	printf(" **r ------ a의 주소 : %p \n", **r);
	printf("***r ------ a의 값   : %d \n", ***r);

	return 0;
}
