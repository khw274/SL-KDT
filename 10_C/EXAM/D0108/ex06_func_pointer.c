// -------------------------------------------------------------------
// 5. 함수 선언 
// -------------------------------------------------------------------
int add(int x, int y);
float div(int x, int y);
int pow(int x, int y);
int sub(int x, int y);
void msg();

// -------------------------------------------------------------------
// 6. 메인 함수 
// -------------------------------------------------------------------
int main(void)
{
	// 함수 포인터 변수 선언 및 초기화
	int (*p_add)(int, int) = add;
	float (*p_div)(int, int) = div;
	void(*p_msg)() = msg;
	int (*p_func[3])(int, int) = { add, pow, sub };		// 차례대로 0, 1, 2

	// 함수이름과 포인터 저장값 확인
	printf("add func : %p, p_add : %p\n", add, p_add);

	// 함수 실행
	printf("p_add(10, 20)    : %d\n", p_add(10, 20));
	printf("p_div(10, 3)     : %.2f\n", p_div(10, 3));
	printf("p_func[0](10, 3) : %d\n", p_func[0](10, 3));
	p_msg();

	return 0;
}
// -------------------------------------------------------------------
// 7. 함수 구현 
// -------------------------------------------------------------------
int add(int x, int y)
{
	return x + y;
}

float div(int x, int y)
{
	return (float)x / y;
}

int pow(int x, int y)
{
	return x * y;
}

int sub(int x, int y)
{
	return x - y;
}

void msg()
{
	printf("Good Luck");
}

