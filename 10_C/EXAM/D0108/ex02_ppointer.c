int main(void)
{

	// 다양한 배열 : 동일한 타입!!
	int nums[] = { 10, 20, 30 };
	int n1 = 100, n2 = 200, n3 = 300;
	int* p_arr[3];

	p_arr[0] = &n1;
	p_arr[1] = &n2;
	p_arr[2] = &n3;

	for (int i = 0; i < 3; i++)
	{
		printf(" p_arr[%d] : %p\n", i, p_arr[i]);
		printf("*p_arr[%d] : %d\n\n", i, *p_arr[i]);
	}

	return 0;
}
