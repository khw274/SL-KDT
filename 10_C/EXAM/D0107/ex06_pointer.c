// -------------------------------------------------------------------
// 5. 함수 선언 
// -------------------------------------------------------------------
void print_value(int a, int b);
void change_value(int* a, int* b);

// -------------------------------------------------------------------
// 6. 메인 함수 
// -------------------------------------------------------------------
int main(void)
{
	// 변수 선언
	int a = 7, b = 8;

	// 값에 의한 호출 Call by Value
	print_value(a, b);
	printf("a = %3d, b = %3d\n", a, b);

	// 참조에 의한 호출 Call by Reference
	change_value(&a, &b);
	printf("a = %3d, b = %3d\n", a, b);

	return 0;
}
// -------------------------------------------------------------------
// 7. 함수 구현 
// -------------------------------------------------------------------
void print_value(int a, int b)
{
	a += 10;
	b *= a;

	printf("a = %3d, b = %3d\n", a, b);
}

void change_value(int* a, int* b)
{
	*a += 10;	// 역참조
	*b *= *a;
}