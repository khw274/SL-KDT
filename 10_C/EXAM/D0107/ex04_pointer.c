int main(void)
{

	// 변수 선언
	int value[] = { 10, 20, 30, 40, 50 };
	int      *p = value;	// 배열명은 첫번째 원소의 주소로 변환 -> &주소 연산자를 안 써도 됨.

	// 정수형 배열 포인터 변수 p, 배열명, 배열 0번원소
	printf("p : 0x%p, value : 0x%p, &value[0] : 0x%p\n\n", p, value, &value[0]);

	int LEN = sizeof(value) / sizeof(value[0]);

	for (int i = 0; i < LEN; i++)
	{
		printf("value[%d] : 0x%p, %d, %d\n", i, p, *p, *(p+i));
	}
	
	return 0;
}
