int main(void)
{

	// 변수 선언
	int value = 127;	// 데이터 저장 변수
	int* p = &value;			// 포인터 변수에 value 변수의 주소 저장

	// 변수 확인
	printf("변수 읽기 &value : %p, value : %d\n", &value, value);
	printf("변수 읽기      p : %p,    *p : %d\n\n", p, *p);

	// 변수 기반 값 변경
	value += 1;

	printf("변수 읽기 &value : %p, value : %d\n", &value, value);
	printf("변수 읽기      p : %p,    *p : %d\n\n", p, *p);

	// 포인터변수 기반 값 변경
	*p += 1;

	printf("변수 읽기 &value : %p, value : %d\n", &value, value);
	printf("변수 읽기      p : %p,    *p : %d\n\n", p, *p);
}
