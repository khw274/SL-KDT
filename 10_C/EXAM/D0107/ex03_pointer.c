int main(void)
{

	// 변수 선언
	int value = 127;			// 데이터 저장 변수
	int* p = &value;			// 포인터 변수에 value 변수의 주소 저장

	// 증감연산자 & 포인터 : 주소 증감
	printf("p : 0x%p, %llu\n", p, (unsigned long long)p);
	p++;
	printf("p : 0x%p, %llu\n", p, (unsigned long long)p);
	++p;
	printf("p : 0x%p, %llu\n\n", p, (unsigned long long)p);

	p--;
	printf("p : 0x%p, %llu\n", p, (unsigned long long)p);
	--p;
	printf("p : 0x%p, %llu\n\n", p, (unsigned long long)p);

	return 0;
}
