int main(void)
{
	// 변수 선언
	char msg[] = "Good";
	char*     p = msg;		// 배열이름 => &배열이름[0] 자동 변환, & 안 붙여도 됨
	
	// 배열이름을 포인터처럼 사용해보기
	printf("*p : %c, msg : %s\n", *p, msg);		// %s -> &배열이름[0] 자동 변환에서 종료문자까지
	printf("*p : %c, msg : %c\n\n", *p, *msg);	// %c -> &배열이름[0] 자동 변환, *&배열이름[0]

	// ★ 배열이름은 포인터 상수가 아님!!
	printf("sizeof(msg) : %zu bytes\n", sizeof(msg));		// char 요소 5개 들어있어서 5 bytes 
	printf("sizeof(p)   : %zu bytes\n"  , sizeof(p));		// 포인터 상수라서 고정 8 bytes

	return 0;
}
