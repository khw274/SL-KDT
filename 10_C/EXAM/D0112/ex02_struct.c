// - 날짜 타입
struct Date {
	int year;
	int month;
	int day;
};

struct UserScore {
	char user_id[8];
	struct Date input_date;
	struct Date change_date;
};

typedef struct UserScore UserScore;

// -------------------------------------------------------------------
// 4. 전역 변수/상수 선언 (Global Variable / constant)
// -------------------------------------------------------------------

// -------------------------------------------------------------------
// 5. 함수 선언 
// -------------------------------------------------------------------

// -------------------------------------------------------------------
// 6. 메인 함수 
// -------------------------------------------------------------------
int main(void)
{
	// 변수 선언 및 초기화
	UserScore s1 = { "s1", {2026, 1, 1}, {2026, 2, 1} };
	UserScore s2 = s1;

	// 사용자 정보 확인 출력
	printf("[s1] id : %s, in : %d - %d, change : %d - %d - %d\n", 
		s1.user_id, 
		s1.input_date.year, s1.input_date.month,s1.input_date.day,
		s1.change_date.year, s1.change_date.month, s1.change_date.day);

	// 사용자 정보 변경
	strcpy(s1.user_id, "newID");//s1.userid = "newID"; -> 배열 선언과 동시에 초기화할 때만 가능
	s1.input_date.year = 2025;
	s1.change_date.year = 2025;
	s1.change_date.month = 12;

	return 0;
}
