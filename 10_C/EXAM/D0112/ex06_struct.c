// -------------------------------------------------------------------
// 3. 구조체 정의 
// -------------------------------------------------------------------
// 학생 타입
struct student {
	int no;
	int kor, eng, math;
};

// 타입 재정의
typedef struct student Student;

// -------------------------------------------------------------------
// 4. 전역 변수/상수 선언 (Global Variable / constant)
// -------------------------------------------------------------------

// -------------------------------------------------------------------
// 5. 함수 선언 
// -------------------------------------------------------------------

// -------------------------------------------------------------------
// 6. 메인 함수 
// -------------------------------------------------------------------
int main(void)
{
	// 변수 선언 및 초기화
	Student st1 = { 1, 100, 100, 100 };
	Student* pst = &st1;

	Student st2 = st1;

	printf("[st1] %d, %d, %d, %d\n", st1.no, st1.kor, st1.eng, st1.math);
	printf("[st2] %d, %d, %d, %d\n", st2.no, st2.kor, st2.eng, st2.math);
	printf("[pst->] %d, %d, %d, %d\n", pst->no, pst->kor, pst->eng, pst->math);

	// 역참조연산자로 멤버 접근
	(*pst).kor = 90;
	pst->kor = 89;
	return 0;
}
