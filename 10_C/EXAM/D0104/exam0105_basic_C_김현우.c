{

	int a, b, c;
	printf("세 정수를 입력하세요 : ");
	scanf("%d %d %d", &a, &b, &c);

	int max = (a > b) ? ((a > c) ? a : c) : ((b > c) ? b : c);
	int min = (a < b) ? ((a < c) ? a : c) : ((b < c) ? b : c);

	printf("세 정수 중 큰 값 : %d, 작은 값 : %d", max, min);
}
