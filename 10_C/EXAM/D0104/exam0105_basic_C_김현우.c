// 8. 정수 x와 k를 입력받아
//     k번째 비트가 1이면 0으로, 0이면 1로 토글하세요.
// -------------------------------------------------------------------------
void exam8()
{
	int x, k;
	printf("정수 x와 k를 입력하세요 : ");
	scanf("%d %d", &x, &k);

	int result = 0;
	result = ((x >> k) & 1);
	//printf("%d", result);

	if (result == 1)
	{
		x = x ^ (1 << k);
	}
	else if (result == 0) {
		x = (x | (1 << k));
	}

	printf("%d", x);
}

