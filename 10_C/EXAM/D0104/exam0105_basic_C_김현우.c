{
	for (int i = 2; i <= 100; i++)
	{
		int sosu = 1;

		for (int j = 2; j * j <= i; j++) // 수의 제곱근까지
		{
			if (i % j == 0)
			{
				sosu = 0;
				break;
			}
		}

		if (sosu == 1) printf("%d ", i);

	}
}

