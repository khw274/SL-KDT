 // -------------------------------------------------------------------------
 // 4. int형 변수 1개를 선언하고 해당 변수의 값과 주소를 출력하세요.
 // -------------------------------------------------------------------------
void exam4()
{
	int a = 123;
	int* b = &a; //포인터 변수 b는 변수 a의 주소를 담고 있음

	printf("\na의 값  : %d\n", b);
	printf("a의 주소: %p\n\n", (void*)b);
}

 // -------------------------------------------------------------------------
 // 5. char 변수에 'A'를 저장하고 해당 문자의 ASCII 코드 값을 출력하세요.
 // -------------------------------------------------------------------------
void exam5()
{
	char a = 'A';

	printf("a의 문자 : %c\n", a);
	printf("a의 ASCII 코드 값 : %d\n\n", (int)a);
 }

 // -------------------------------------------------------------------------
 // 6. long long 변수에 10^12 이상의 값을 저장하고 출력하세요.
 // -------------------------------------------------------------------------
void exam6() {
	long long a = 10LL;

	for (int i = 0; i < 12; i++) 
	{
		a *= 10;
	}

	printf("a의 값 : %lld\n\n", a);
}

