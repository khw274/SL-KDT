#include <stdio.h>

 // =========================================================================
 // 단답형 (정답)
 // =========================================================================
 // -------------------------------------------------------------------------
 // 1. C언어에서 정수형의 기본 자료형은 무엇인가요?
 // -------------------------------------------------------------------------
 // 정답: int

 // -------------------------------------------------------------------------
 // 2. char 자료형의 크기는 몇 바이트인가요?
 // -------------------------------------------------------------------------
 // 정답: 1바이트

 // -------------------------------------------------------------------------
 // 3. signed와 unsigned의 차이는 무엇인가요?
 // -------------------------------------------------------------------------
 // 정답: signed는 부호(±)가 있고, unsigned는 부호가 없으며 0 이상만 표현한다.

 // -------------------------------------------------------------------------
 // 4. int형이 표현할 수 있는 값의 범위는 무엇에 의해 결정되나요?
 // -------------------------------------------------------------------------
 // 정답: 비트 수(자료형의 크기, sizeof(int))와 부호 유무(signed/unsigned)에 
 //       의해 결정된다.

 // -------------------------------------------------------------------------
 // 5. 실수형 자료형 2가지를 쓰세요.
 // -------------------------------------------------------------------------
 // 정답: float, double

 // -------------------------------------------------------------------------
 // 6. float과 double의 가장 큰 차이점 한 가지를 쓰세요.
 // -------------------------------------------------------------------------
 // 정답: double이 float보다 정밀도(유효자릿수)와 표현 범위가 더 크다.
 //       float(32 - bit)  : 유효숫자 약 6~9자리, 보통 ~7자리
 //       double(64 - bit) : 유효숫자 약 15~17자리, 보통 ~15자리

 // -------------------------------------------------------------------------
 // 7. sizeof 연산자의 역할은 무엇인가요?
 // -------------------------------------------------------------------------
 // 정답: 자료형 또는 변수(객체)의 메모리 크기(바이트)를 구한다.

 // ------------------------------------------------------------------------- 
 // 8. sizeof의 반환값 단위, 반환 타입은 무엇인가요?
 // -------------------------------------------------------------------------
 // 정답: 반환값 단위 - 바이트(byte)
 //       반환값 타입 - size_t  ==> windows64는 unsigned long long
 //       형식 지정자 = %zu      

 // ------------------------------------------------------------------------- 
 // 9. 문자 'A'를 저장하는 데 사용하는 자료형은 무엇인가요?
 // -------------------------------------------------------------------------
 // 정답: char

 // -------------------------------------------------------------------------
 // 10. 문자열을 저장할 때 사용하는 기본 자료형은 무엇인가요?
 // -------------------------------------------------------------------------
 // 정답: char 배열 (char[])


 // =========================================================================
 // 코드형 (모범 답안)
 // =========================================================================

int main(void)
{
    // ---------------------------------------------------------------------
    // 1. char, short, int, long long의 sizeof를 출력하는 프로그램을 작성하세요.
    // ---------------------------------------------------------------------
    printf("[1] sizeof 출력\n");
    printf("sizeof(char)      = %zu byte\n", sizeof(char));
    printf("sizeof(short)     = %zu byte\n", sizeof(short));
    printf("sizeof(int)       = %zu byte\n", sizeof(int));
    printf("sizeof(long long) = %zu byte\n\n", sizeof(long long));

    // ---------------------------------------------------------------------
    // 2. unsigned int로 0부터 시작해 5번 1씩 감소시키며 출력해 보세요.
    //    (언더플로우 현상 확인)
    // ---------------------------------------------------------------------
    printf("[2] unsigned int 언더플로우 확인\n");
    {
        unsigned int num = 0;
        for (int i = 0; i < 5; i++)
        {
            printf("num = %u\n", num);
            num--;
        }
        printf("\n");
    }

    // ---------------------------------------------------------------------
    // 3. int, float, double 변수를 각각 선언하고 값을 저장한 뒤 출력하세요.
    // ---------------------------------------------------------------------
    printf("[3] int/float/double 출력\n");
    {
        int    a = 10;
        float  b = 3.5f;
        double c = 7.25;

        printf("int    a = %d\n", a);
        printf("float  b = %f\n", b);
        printf("double c = %lf\n\n", c);
    }

    // ---------------------------------------------------------------------
    // 4. int형 변수 1개를 선언하고 해당 변수의 값과 주소를 출력하세요.
    // ---------------------------------------------------------------------
    printf("[4] int 값과 주소 출력\n");
    {
        int x = 123;
        printf("x 값   = %d\n", x);
        printf("x 주소 = %p\n\n", (void*)&x);
    }

    // ---------------------------------------------------------------------
    // 5. char 변수에 'A'를 저장하고 해당 문자의 ASCII 코드 값을 출력하세요.
    // ---------------------------------------------------------------------
    printf("[5] char 'A'와 ASCII 코드 출력\n");
    {
        char ch = 'A';
        printf("ch 문자 = %c\n", ch);
        printf("ASCII   = %d\n\n", (int)ch);
    }

    // ---------------------------------------------------------------------
    // 6. long long 변수에 10^12 이상의 값을 저장하고 출력하세요.
    // ---------------------------------------------------------------------
    printf("[6] long long 큰 값 출력\n");
    {
        long long big = 1000000000000LL;  // 10^12
        printf("big = %lld\n\n", big);
    }

    // ---------------------------------------------------------------------
    // 7. 사용자로부터 정수 1개를 입력받아 sizeof를 사용해 해당 자료형의 크기를 출력하세요.
    //    (입력값 자체가 아니라, int형 크기 출력)
    // ---------------------------------------------------------------------
    printf("[7] 정수 입력 후 int 크기(sizeof) 출력\n");
    {
        int n;
        printf("정수 1개 입력: ");
        if (scanf("%d", &n) != 1) return 0;

        printf("입력값 n = %d\n", n);
        printf("sizeof(n) = %zu byte\n\n", sizeof(n));
    }

    // ---------------------------------------------------------------------
    // 8. int형 변수 2개를 선언하고 두 변수의 주소를 각각 출력하세요.
    // ---------------------------------------------------------------------
    printf("[8] int 변수 2개 주소 출력\n");
    {
        int a = 1, b = 2;
        printf("a 주소 = %p\n", (void*)&a);
        printf("b 주소 = %p\n\n", (void*)&b);
    }

    // ---------------------------------------------------------------------
    // 9. float 변수에 3.14를 저장하고 소수점 2자리까지 출력하세요.
    // ---------------------------------------------------------------------
    printf("[9] float 3.14 소수점 2자리 출력\n");
    {
        float f = 3.14f;
        printf("f = %.2f\n\n", f);
    }

    // ---------------------------------------------------------------------
    // 10. char형 변수에 숫자 48를 저장하고 문자로 출력되는 결과를 확인하세요.
    // ---------------------------------------------------------------------
    printf("[10] char에 65 저장 후 문자 출력\n");
    {
        char c = 48;
        printf("c(정수) = %d\n", (int)c);
        printf("c(문자) = %c\n", c); // 'A'가 출력됨(ASCII 65)
    }

    return 0;
}
