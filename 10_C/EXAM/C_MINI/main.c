int main(void)
{

	int menu = 0;

	while (1)
	{
		// 메뉴 출력
		printf("================ 플레이봇 ================\n\n");
		printf("              1. 사과 게임\n");
		printf("              2. 검 키우기 게임\n");
		printf("              3. 틱택톡 게임\n");
		printf("              4. 검 키우기 게임\n");
		printf("              5. 게임 종료\n\n");
		printf("===========================================\n");
		printf("게임을 선택하세요 : "); scanf_s("%d", &menu);

		// 선택 시 게임 진행 함수 불러옴
		switch (menu) {
		case 1:
			loading();
			break;
		case 2:
			loading();
			break;
		case 3:
			loading();
			break;
		case 4:
			loading();
			break;
		case 5:
			printf("게임을 종료합니다.\n");
			break;
		default:
			printf("잘못된 입력입니다.\n");
			printf("다시 입력해주세요!\n\n");

			Sleep(1000);
			system("cls");

			continue;
		}

		return 0;
	}
}
